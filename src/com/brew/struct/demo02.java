package com.brew.struct;

public class demo02 {
    public static void main(String[] args) {
        int i  = 0;
        while (i<10){
            i++;
            System.out.println(i); // 1-10
        }

        int b=0;
        do{
            b++;
            System.out.println(b); // 1-10
        }while (b<10);

        for (int j = 0; j < 10; j++) {
            System.out.println(j); // 0-9
        }


    }
}
